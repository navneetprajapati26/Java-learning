package assinmant_7_8;

import java.util.Scanner;

public class week {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int week = sc.nextInt();
        switch (week) {
            case 1:
                System.out.println("monday");
                break;

            case 2:
                System.out.println("tuseday");
                break;

            case 3:
                System.out.println("wasnesday");
                break;

            case 4:
                System.out.println("thseday");
                break;

            case 5:
                System.out.println("fryday");
                break;

            case 6:
                System.out.println("satrday");
                break;

            case 7:
                System.out.println("sonday");
                break;

        }
    }

}
