package day_6_8_22;

public class relationnal_op {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("is a==b :-" + (a == b));
        System.out.println("is a!=b :-" + (a != b));
        System.out.println("is a>b :-" + (a > b));
        System.out.println("is a<b :-" + (a < b));
        System.out.println("is a>=b :-" + (a >= b));
        System.out.println("is a<=b :-" + (a <= b));
    }

}
