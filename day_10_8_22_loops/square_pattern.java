package day_10_8_22_loops;

public class square_pattern {
    public static void main(String[] args) {
        for (int i = 1; i <= 4; i++) {
            System.out.println("*****");
        }

        System.out.println();
        System.out.println();

        int a = 1;
        while (a <= 4) {
            System.out.println("*****");
            a++;
        }
    }

}
